/*
 * ROMFS files header for OndOcean RemoteID
 * Embedded filesystem for web interface
 */

#pragma once

#include <stdint.h>

// ROMFS file structure
struct romfs_file {
    const char *name;
    const uint8_t *data;
    uint32_t size;
};

